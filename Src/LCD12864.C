/*
****************************************************
*  Function       : LinkAdd
*  Description    : 添加链表
*  Calls          : 
*  Called By      : 
*  Input          : Head：链表头  Added：新加元素
*  Output         : 
*  Return         : 失败返回非 0 值
*  Others         : 
*****************************************************
*/
uint8_t LinkAdd(pFONT_MANAGE Head,pFONT_MANAGE Added)
{
	pFONT_MANAGE Temp = Head;
	uint8_t Cnt = 0;
	
	while(Temp->NEXT != 0x00 && Cnt++ < 128)Temp = Temp->NEXT;
	
	if(Cnt < 128)
	{
		Temp->NEXT = Added;
		return 0x00;
	}
	return Cnt;
}

